__________RIAS GREMORY__________()
{
		ios_base::sync_with_stdio(false);
		cin.tie(NULL);
		cout.tie(NULL);
		srand(time(NULL));
		int cnt = 0;
		int level = 1;
		int n;
		cout << "Level 1 -> 5 : Easy" << endl;
		cout << "Level 6 -> 10 : Normal " << endl;
		cout << "Level 11++ : Hard " << endl;
		cout << "Nhap so cau : ";
		cin >> n;
		cout << endl;
		for (int i = 0; i < n; i++)
		{
			cout << "Cau " << i + 1 << endl;
			cout << "So diem : " << cnt << endl;
			cout << "Level : " << level << endl;
			if (level <= 5)
			{
				Bieuthuc bt(level);
				cout << bt;
				float traloi;
				cin >> traloi;
				if (bt.kiemtra(traloi))
				{
					cout << "YES" << endl;
					cnt++;
					level++;
				}
				else
				{
					cout << "NO" << endl;
					level--;
					if (level<= 0)
					{
						level = 1;
					}
				}
			}
			else if (level > 5 && level < 10)
			{
				BieuthucPT pt(level);
				cout << pt;
				float traloi;
				cin >> traloi;
				if (pt.kiemtra(traloi))
				{
					cout << "YES" << endl;
					cnt++;
					level++;
				}
				else
				{
					cout << "NO" << endl;
					level--;
				}
			}
			else
			{
				BieuthucPS ps(level);
				cout << ps << endl;
				cout << ps.giatri() << endl;
				Fraction traloi;
				cin >> traloi;
				if (ps.kiemtra(traloi))
				{
					cout << "YES" << endl;
					cnt++;
					level++;
				}
				else
				{
					cout << "NO" << endl;
					level--;
				}
			}
		}
		cout << "So diem : " << cnt << "/" << n << endl;
}

